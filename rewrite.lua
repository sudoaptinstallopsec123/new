    else
        warn("[Teleport] No CFrame entry for island: " .. island.Name)
    end
end

local function randomTeleport(root, island)
    if not island then
        warn("[RandomTeleport] Island is nil, skipping.")
        return
    end
    local teleports = IslandTeleports[island.Name]
    if not teleports or #teleports == 0 then
        warn("[RandomTeleport] No teleport entries for: " .. island.Name)
        return
    end
    root.CFrame = teleports[math.random(1, #teleports)]
end

local function getNearestHorse(root, island)
    local nearest  = nil
    local shortest = math.huge

    -- If wildherd_capture is enabled, first scan workspace for wild herd horses
    -- Wild herd horses take priority over regular horses
    if wildherd_capture then
        for _, obj in ipairs(workspace:GetChildren()) do
            if obj:IsA("Model") then
                local horseRoot = obj:FindFirstChild("HumanoidRootPart")
                if horseRoot then
                    local dist = (root.Position - horseRoot.Position).Magnitude
                    if dist < shortest then
                        shortest = dist
                        nearest  = horseRoot
                    end
                end
            end
        end

        -- If a wild herd horse was found, return it immediately (priority)
        if nearest then
            return nearest
        end
    end

    -- Fall through to regular horses (CaptureProgress) either when:
    -- wildherd_capture is off, or no wild herd horses were found
    for _, obj in ipairs(island:GetDescendants()) do
        if obj:IsA("Model") then
            local horseRoot  = obj:FindFirstChild("HumanoidRootPart")
            local captureGui = obj:FindFirstChild("CaptureProgress", true)

            if horseRoot and captureGui then
                local dist = (root.Position - horseRoot.Position).Magnitude
                if dist < shortest then
                    shortest = dist
                    nearest  = horseRoot
                end
            end
        end
    end

    return nearest
end


local function getNextFarmIsland(currentIslandName)
    local enabled = {}
    for _, name in ipairs(AVAILABLE_ISLANDS) do
        if autofarm_islands[name] then
            table.insert(enabled, name)
        end
    end

    if #enabled == 0 then return nil end
    if #enabled == 1 then return enabled[1] end

    for i, name in ipairs(enabled) do
        if name == currentIslandName then
            return enabled[(i % #enabled) + 1]
        end
    end

    return enabled[1]
end

