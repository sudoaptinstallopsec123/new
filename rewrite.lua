local function applyColor()
    if not currentHighlight then return end
    currentHighlight.FillColor    = getgenv().Highlight_FillColor
    currentHighlight.OutlineColor = getgenv().Highlight_OutlineColor
end

local function playAnim()
    if not currentHighlight then return end

    stopTween()

    if not getgenv().Highlight_Enabled then
        currentHighlight.Enabled = false
        return
    end

    currentHighlight.Enabled             = true
    currentHighlight.FillColor           = getgenv().Highlight_FillColor
    currentHighlight.OutlineColor        = getgenv().Highlight_OutlineColor
    currentHighlight.FillTransparency    = 0.5
    currentHighlight.OutlineTransparency = 0

    local info = TweenInfo.new(
        getgenv().Highlight_Speed,
        Enum.EasingStyle.Sine,
        Enum.EasingDirection.InOut,
        -1,
        true
    )

    local mode = getgenv().Highlight_Mode

    if mode == "Pulse" then
        currentTween = TweenService:Create(currentHighlight, info, {
            FillTransparency    = 0.9,
            OutlineTransparency = 0.9
        })
    elseif mode == "Breathe" then
        currentHighlight.OutlineTransparency = 0
        currentTween = TweenService:Create(currentHighlight, info, {
            OutlineTransparency = 0.8
        })
    elseif mode == "Shift" then
        currentTween = TweenService:Create(currentHighlight, info, {
            FillColor    = getgenv().Highlight_FillColor,
            OutlineColor = getgenv().Highlight_OutlineColor
        })
    elseif mode == "Flash" then
        currentTween = TweenService:Create(currentHighlight, info, {
            FillTransparency = 0
        })
    elseif mode == "None" then
        -- Static highlight, no animation
    end

    if currentTween then currentTween:Play() end
end

